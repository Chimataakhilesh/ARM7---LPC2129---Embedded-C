#include<LPC21XX.H>
#include "lcd_header.h"

unsigned char cgram_lut[6][8]= {{0x0E,0x1B,0x11,0x11,0x11,0x11,0x1F,0x00},
																{0x0E,0x1B,0x11,0x11,0x11,0x1F,0x1F,0x00},
																{0x0E,0x1B,0x11,0x11,0x1F,0x1F,0x1F,0x00},
																{0x0E,0x1B,0x11,0x1F,0x1F,0x1F,0x1F,0x00},
																{0x0E,0x1B,0x1F,0x1F,0x1F,0x1F,0x1F,0x00},
																{0x0E,0x1F,0x1F,0x1F,0x1F,0x1F,0x1F,0x00}};
void cgram_write(void);

int main()
{
	int frame=0;
	LCD_INIT();
	cgram_write();
	while(1){
	LCD_COMMAND(0x80);
	LCD_DATA(frame);
	delay_ms(250);
	if(frame>=5)
		frame=0;
	frame++;
}
	
}
void cgram_write(void)
{
	int i=0,j=0;
	LCD_COMMAND(0x40);
	for(j=0;j<6;j++){
		for(i=0;i<8;i++)
	  LCD_DATA(cgram_lut[j][i]);
	}
}

